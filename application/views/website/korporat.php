                                        										<h2>TVS Motor Company</h2>
										<p align="justify">
TVS Motor Company adalah salah satu perusahaan manufaktur roda dua terbesar di India dan 10 besar di dunia yang merupakan perusahaan terbesar di TVS Group yang bernilai 5 milyar US Dollar.

<br><br><b>Motor Untuk Siapa Saja</b><br><br>

TVS Motor saat ini memproduksi jajaran luas kendaraan roda dua dari moped, ke sepeda motor inspirasi balap. 

<br><br><b>Sepeda Motor</b>  (TVS Apache RTR 180 ABS, TVS Apache RTR 180, TVS Apache RTR 160, TVS Jive, TVS Star City, TVS Sport)  

<br><br><b>Skuter Matic</b>  (TVS Wego, TVS Scooty Streak, TVS Scooty Pep+, TVS Scooty Teenz) 

<br><br><b>Moped</b>  (TVS XL Super, TVS XL Heavy Duty)

<br><br><b>Kegemaran Akan Kualitas</b>

<br><br>TVS Motor Company memiliki 3 pabrik kelas dunia di India, berlokasi di Hosur dan Mysore di India Selatan, di Himachal Pradesh, India Utara, dan satu di Indonesia. Perusahaan ini memiliki kapasitas produksi sebesar 2,5 juta unit per tahun.

<br><br><b>Inovasi Dalam Genggaman</b>

<br><br>Keunggulan TVS Motor terletak di desain dan pengembangan produk baru. Keinginan pelanggan yang terus berubah merupakan sumber inspirasi kami yang terus berkelanjutan. Kami di TVS menyediakan kepuasan total pelanggan dengan cara mengantisipasi segala kebutuhan yang muncul dari mereka dan menghadirkan kendaraan berkualitas di saat yang tepat dengan harga yang pas. TVS Motor telah menerima penghargaan Deming untuk Total Quality Management dari Union of Japanese Scientists & Engineers dan merupakan satu-satunya perusahaan penghasil roda dua di dunia yang pernah menerima penghargaan prestisius ini. TVS Motor juga telah menerima TPM Excellence Award dari Japan Institute of Plant Maintenance. Divisi riset dan pengembangan TVS Motor terdiri dari 400 ahli teknologi yang menciptakan inovasi revolusioner, termasuk VT-I (Variable Timing Intelligent) dan mode kekuatan-ekonomis, dan telah memiliki lebih dari 150 paten. TVS Motor Company menghasilkan varian sepeda motor dan skuter yang telah diekspor ke 50 negara, termasuk didalamnya TVS Apache dan Scooty Pep+ yang telah menerima banyak penghargaan.

<br><br><b>Lima Belas Juta Senyuman Di Jalan</b>

<br><br>TVS selalu berdiri tegak dalam produk yang inovatif, mudah dikendarai, dan ramah lingkungan, dilengkapi dengan pelayanan pelanggan yang dapat diandalkan. 

<br><br>Tidak mengherankan apabila 15 juta pelangggan kami di jalan memiliki alas an untuk tersenyum. 
